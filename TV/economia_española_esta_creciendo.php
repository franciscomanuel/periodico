					<p>La recuperación de la economía española se afianzó en el primer trimestre del año con un crecimiento del 0,4% respecto a los tres trimestres anteriores, según el dato adelantado este jueves por el Banco de España, que supone una aceleración respecto a las tasas registradas en el tercer y cuarto trimestres de 2013 (del 0,1% y del 0,2%), que marcaron el final de una larga recesión. Para encontrar un ritmo de crecimiento mayor hay que dar marcha atrás en el tiempo hasta el inicio de la crisis, cuando a comienzos de 2008 la economía creció un 0,5% para entrar poco después en el túnel de la llamada Gran Recesión.</p>

					<p>En comparación con el primer trimestre del año pasado, el avance en el primer trimestre de este año es del 0,5%, lo que supone la primera tasa positiva interanual tras nueve trimestres consecutivos de caída, según el boletín económico de la institución.</p>

					<p>El ligero repunte del consumo privado y la inversión empresarial impulsaron el crecimiento: la demanda doméstica aporta dos décimas del avance del primer trimestre. También han una pequeña mejora del gasto y la inversión de las administraciones públicas. Las otras dos décimas de crecimiento trimestral las aporta el sector exterior, que ralentiza su expansión respecto a la registrada en el tramo final del 2013.</p>

					<p>Con estos datos, el Banco de España confirma sus previsiones oficiales, avanzadas en marzo, que sitúan el ritmo de la recuperación con un crecimiento del 1,2% y el 1,7% en 2014 y 2015. Estas proyecciones están en línea con las que maneja el gabinete de Mariano Rajoy. El ministro de Economía, Luis de Guindos, vaticinó este miércoles que el PIB crecerá de media un 1,5% este año y el siguiente.</p>

					<p>Respecto al riesgo de deflación, el Banco de España considera "remota" la posibilidad de que se registren caídas generalizadas y persistentes de los precios de consumo, y solo la contempla en el caso de que un riesgo de esta naturaleza se materialice en toda la zona del euro.</p>

					<p>La entidad supervisora sí espera tasas de variación muy reducidas del IPC a lo largo del año, que pueden ser incluso puntualmente negativas. Pero minimiza el riesgo de deflación al esperar una trayectoria "ligeramente creciente" de la inflación en el transcurso de 2014 debido a la tendencia de recuperación del gasto de los hogares y a las menores caídas en los costes laborales (principalmente salarios).</p>

