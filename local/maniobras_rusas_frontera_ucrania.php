					<p>Moscú elevó este jueves de nuevo el tono en el conflicto en torno a Ucrania al ordenar maniobras militares en la frontera común. Fue la respuesta a la actuación de los comandos ucranios en el bastión rebelde de Slaviansk, según manifestó el ministro ruso de Defensa, Serguéi Shoigu. “Nos hemos visto obligados a reaccionar”, dijo. “Si la maquinaria militar [ucrania] no se detiene, ello llevará a un aumento del número de muertos y heridos”, advirtió.</p>

					<p>Las maniobras militares se desarrollarán en los distritos del sur y del oeste de Rusia, en la zona fronteriza con el país vecino. Está prevista la intervención de la Fuerza Aérea, concretó el titular de Defensa. Añadió también que “los ejercicios militares previstos por las fuerzas de la OTAN en Polonia y los países bálticos no fomentan la normalización de la situación en torno a Ucrania”.</p>

					<p>Antes que el ministro, había hablado el presidente ruso, Vladímir Putin, quien criticó a las autoridades de Kiev por su actuación en la zona de Slaviansk, convertida en el foco más tenso del conflicto entre las autoridades ucranias y los rebeldes prorrusos. “Si es verdad que el actual régimen de Kiev empezó a utilizar al Ejército contra la población dentro del país, eso, sin duda alguna, significa un crimen muy grave contra su propio pueblo”, afirmó el jefe del Estado en San Petersburgo.</p>

					<p>Putin advirtió de que la “operación de castigo” en Slaviansk “tendrá consecuencias para los que toman semejantes decisiones, así como para las relaciones interestatales [entre Rusia y Ucrania]”. Agregó que, si se confirman las informaciones sobre el asalto de las fuerzas especiales ucranias a Slaviansk, a las autoridades de Kiev se las podrá denominar “junta”. “Carecen de mandato nacional. Tienen a lo sumo algunos elementos de legitimidad, pero solo en el Parlamento. Las demás autoridades, por varias razones, no son legítimas”, subrayó el presidente.</p>

					<p>El jefe del Estado también aludió a Crimea, provincia ucrania de mayoría rusohablante anexionada por Rusia tras el referéndum celebrado el pasado mes de febrero. “Ahora todos se dan cuenta de que, sin el apoyo real de Rusia a la gente que vive en Crimea, sería imposible realizar el proceso civilizado de la expresión de voluntad de los ciudadanos. La situación sería igual a lo que está pasando en estos momentos en el este y sur de Ucrania. Pasaría lo mismo, o peor aún. Así que todo eso demuestra, una vez más, que hemos hecho todo correctamente y a tiempo”, agregó el presidente Putin. A su juicio, para encontrar una solución al conflicto, las autoridades de Kiev deben empezar un diálogo pacífico dentro del país, en vez de ir legalizando las organizaciones radicales.</p>

