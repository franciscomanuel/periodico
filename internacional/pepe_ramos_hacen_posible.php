					<p>La charla de Carlo Ancelotti antes del partido concentró un mensaje esencial, telúrico, propio de su condición de provinciano. Porque, como decía su maestro, Arrigo Sacchi, se trata de una idea instalada en la conciencia del campesinado italiano desde la Edad Media. El entrenador del Madrid les dijo a sus jugadores que lo primero era defender todos juntos a la espera de una oportunidad y que, si esa oportunidad no llegaba en Chamartín, ya llegaría en Múnich, o nunca, pero que lo importante era prolongar la esperanza en la eliminatoria. Había que resistir como aquellos aldeanos sometidos por el bárbaro. Había que aguantar. Había que sobrevivir como fuese, aunque fuera a base de pan duro y lentejas.</p>

					<p>Defender, defender, defender. Eso hizo el Madrid ante las cascadas del Bayern, que avanzó desde fuera con Robben y Ribéry, por todas partes con Alaba, por el medio con Mandzukic, y desde la segunda línea con los alemanes, con Lahm, Schweinsteiger y Kroos. Una multitud invadió las praderas del Madrid, que se atrincheró alrededor de su área, donde destacaron los centrales. Los verdaderos héroes de esta estrategia. Pepe y Ramos hicieron las labores oscuras y silenciosas. Pusieron el cuerpo, metieron las piernas, los brazos, saltaron, cortaron, despejaron. Sin ellos el plan habría fracasado. Estuvieron espléndidos, rápidos, astutos para anticiparse. Dice Ancelotti que desde hace un par de meses le han quitado las dudas: ya sabe que Pepe y Ramos son los mejores centrales del mundo. En este momento no hay otros dos más en forma. Son la clase de pilares que sirven de apoyo a los equipos campeones.</p>

					<p>El Madrid no salió apenas de su campo y tuvo el 27% de la posesión durante el primer tiempo de partido. No fue una estadística gloriosa cuando se trata del club que más dinero ha invertido en fichajes en la última década, con más de 1.000 millones de euros. Una cantidad inaudita en la historia de la industria del deporte. Difícil de justificar solo con títulos, y tampoco los títulos han abundado últimamente.</p>

					<p>El presidente, Florentino Pérez, dice que su proyecto busca la belleza. Eso le pidió a Ancelotti cuando lo contrató y el entrenador se comprometió en su presentación a promover un fútbol “espectacular”. Estas aspiraciones, sin embargo, chocaron con la práctica. El entrenador lo argumenta con razones técnicas: los elementos más competitivos de la plantilla son aquellos que se expresan mejor contragolpeando, tal vez porque durante tres años Mourinho trabajó y contrató para hacer ese fútbol. De modo que el Madrid contragolpea. Le ganó la Copa al Barça contragolpeando y también contragolpeó al Bayern.</p>

					<p>Antes de contragolpear, sin embargo, es necesario defender. El Madrid, en este punto, tampoco goza de abundantes especialistas. Modric es mediapunta, Di María es extremo, Carvajal y Coentrão se manejan mejor cuando tienen el balón que sin él, y los delanteros ayudan lo mínimo imprescindible, y a veces tampoco eso. La contradicción parecía diabólica pero Ancelotti le encontró un remedio: Xabi Alonso cuida que todos estén en su sitio y Ramos y Pepe levantan la empalizada. Eso hicieron contra el Bayern, que no encontró el modo de filtrar pases interiores y acabó casi todas las jugadas centrando balones a la olla. Ahí destacaron Pepe y Ramos. Sobre todo Pepe, que en el apartado de los despejes de cabeza acabó llevándose la mayoría de duelos. En la primera mitad el equipo alemán disparó nueve veces a puerta contra seis del Madrid. Pero fueron tiros filtrados, sin precisión, rebotados muchas veces en las piernas de los defensas locales.</p>

					<p>Mandzukic, el destinatario principal de los centros, fue reducido a la impotencia. Otra buena intuición de Ancelotti, que bromeó estos días sobre el delantero centro croata. El entrenador dijo que se trataba de un excelente punta pero que, en el sistema de Guardiola, se encontraba despistado. Al hilo de esta observación añadió que Mandzukic, si jugaba, se convertiría en el tercer central del Madrid, un verdadero tarugo en los avances de su propio equipo. Presa fácil para Pepe y Ramos, que lo tomaron alternativamente sin darle tregua. Pepe fue sustituido por lesión en el minuto 73 y el estadio emitió un grito de adoración: “¡Peeeeepe, Peeeeepe…!”.</p>	

