					<p>Con la intención de que las manifestaciones prorrusas de este fin de semana en varias de las principales ciudades del Este de Ucrania no se conviertan en el preludio de otra Crimea, el secretario de Estado norteamericano, John Kerry, ha llamado a su homólogo ruso, Serguéi Lavrov, para advertirle de las consecuencias de una nueva incursión militar en el país vecino. Kerry, sin embargo, ha dejado abierta la puerta a la diplomacia y ambos dirigentes han acordado convocar en los próximos diez un reunión para trara de resolver la crisis que, además de a sus respectivas naciones, incluya a Ucrania y la Unión Europea, ha anunciado este lunes el Departamento de Estado.</p>

					<p>La advertencia de Kerry es similar a la que la Casa Blanca ha lanzado al Kremlin. "Pedimos al presidente Putin y a su Gobierno que cese cualquier intención de desestabilizar Ucrania y le prevenimos contra futuras intervenciones militares", ha señalado esta mañana el portavoz de la presidencia, Jay Carney. Las consecuencias de una nueva incursión de tropas se traducirán en otra  tanda de sanciones, ha explicado Carney. Las nuevas medidas podrían afectar a sectores importantes de la economía rusa, como la industria petrolífera o el gas, pero la Administración, sin embargo, ha eludido precisar el alcance de los castigos.</p>

					<p>De acuerdo con el Departamento de Estado, Kerry ha pedido a Lavrov que Rusia “rechace públicamente las acciones de los separatistas, provocadores y saboteadores”, en alusión a las manifestaciones del pasado domingo en las ciudades ucranias de Kharkiv, Luhansk y Donetsk en las que activistas prorrusos tomaron varios edificios gubernamentales exigiendo la celebración de un referéndum secesionista. El secretario de Estado puso en duda, además, la "espontaneidad" de esas protestas, haciéndose eco de las denuncias de Kiev. "Le hizo notar [a Lavrov] que parecía una campaña cuidadosamente orquestada con el apoyo de Rusia", ha indicado la portavoz del Departamento de Estado, Jen Psaki.</p>

					<p>Durante la intervención militar de Crimea, que también se vio precedida de manifestaciones a favor de Moscú similares a las de este domingo, el Kremlin fue acusado de enviar ciudadanos rusos para inflar la magnitud del apoyo. El portavoz de la Casa Blanca ha señalado este lunes que hay "pruebas contundentes" que "sugieren que fuerzas ajenas, no locales, han tomado parte en la creación de estas provocaciones".</p>

					<p>Con todo, EE UU sigue apostando por el diálogo para mitigar la tensión y tratar de buscar una solución dialogada a la crisis. El Departamento de Estado ha anunciado que Kerry y Lavrov han convenido en iniciar el los próximos diez días una tanda de negociaciones directas que incluyan a EE UU, Rusia, Ucrania y la Unión Europea para buscar atemperar la tensión en la zona. Su encuentro imprevisto de París de hace una semana no logró avances concretos, si bien, ambos dirigentes acordaron explotar la vía diplomática. Desde entonces, los dos mandatarios han mantenido conversaciones fluidas para tratar de acercar posturas entre sus diferentes propuestas. Washington pide la retirada de todas las tropas rusas de Ucrania y la frontera, un control internacional de la situación y conversaciones directas entre Kiev y el Kremlin; Moscú la federalización del país vecino.</p>

