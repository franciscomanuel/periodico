					<p>Los días comienzan a resumirse en partes de guerra, que están muy contaminados por la propaganda omnipresente tanto por parte de Kiev como de Moscú. El jueves, el Ministerio del Interior de Ucrania informó de cinco personas muertas en tres controles en las inmediaciones de Slaviansk. Amnistía Internacional criticó al ministerio ucranio por referirse a las víctimas como “cinco terroristas exterminados”. “De acuerdo con las normas internacionales, los funcionarios deben emplear las armas de fuego solo si hay una amenaza indiscutible para la vida o un serio peligro para la salud”, dijo Amnistía.</p>

					<p>Sobre el terreno, en el centro de esa ciudad que es la avanzadilla de las milicias de la RPD, unos paisanos informaban de un ataque a un puesto de control en el acceso por el norte, en una localidad llamada Jrestyshche.</p>

					<p>En ese lugar, varios carros blindados, según unos siete, según otros diez, habían atacado a los mozos del pueblo que custodiaban el puesto, uno de los que se interpone en las rutas desde Izium (el centro coordinador de la “operación antiterrorista” en el sur de la provincia de Járkov) a Slaviansk, al norte de Donetsk. En Bylbasovka había otro de esos puestos, donde en la noche del pasado sábado perecieron tres activistas de la RPD.</p>

					<p>Los atacantes en Jrestyshche eran uniformados ucranios bien equipados, señalaron unos vecinos de Slaviansk. Según medios implicados en las estructuras de defensa de la RPD, podía tratarse de unidades del grupo de intervención especial OMEGA. Tras la incursión, los blindados se retiraron de la posición atacada, señalaron. El centro de Slaviansk estaba tranquilo por la tarde, algunos niños montaban en bicicleta junto a la estatua de Lenin, pero los columpios del parque instalado al lado de la alcaldía estaban vacíos, en contraste con los muchos niños que jugaban aquí hace tres días. En el puesto de control número uno de la RPD en la salida de Kramatorsk hacia Slaviansk, un activista llamado Artiom aseguraba que cuatro helicópteros habían despegado por la mañana del vecino aeropuerto (controlado por Kiev) con rumbo a Slaviansk y que solo dos habían vuelto. También informaba de la llegada de un camión militar cargado de uniformados ucranios.</p>

					<p>Otros incidentes del jueves incluían la incursión de un grupo de individuos con ropa de civil que, de madrugada, irrumpieron con palos, hierros y otros objetos contundentes en el ayuntamiento de Mariúpol (al sur de la provincia de Donetsk, en la costa del mar de Azov) y golpearon brutalmente a los activistas de la RPD que lo ocupaban, según fuentes vinculadas a la defensa de los prorusos. La policía, dijeron, no hizo nada por detener a los atacantes, que se marcharon tras concluir su acción punitiva. La agencia AFP se refirió a cinco heridos en Mariúpol.</p>

					<p>Además, según medios de la RDP se registró un enfrentamiento entre milicias y fuerzas gubernamentales en la ciudad de Sviatogorsk (al norte de la provincia) y un asalto de los milicianos a una unidad militar en Artiomsk, aparentemente con la intención de apoderarse de un importante arsenal. En este incidente se habría registrado un herido, según la BBC. Desde Artiomsk, un corresponsal de este medio vio ventanas y puertas rotas, pero pocas huellas de una batalla, aunque el Ministerio del Interior de Ucrania dijo que la unidad fue atacada por unos 70 milicianos armados.</p>

					<p>Así las cosas, Rusia se puso a exhibir músculo y a reforzar el mensaje ya expresado por sus dirigentes, a saber que no dejarán que se persiga a los rusos de Ucrania.</p>

					<p>La difusión de los actos de violencia se suma a la crisis económica que afecta la vida en Donetsk, una ciudad que modernizó sus infraestructuras para el campeonato de Europa de fútbol de 2012, ya que su principal oligarca, Rinat Ajmétov, la primera fortuna del país, es el gran patrón del equipo local Shajtior. En su principal avenida, hay muchas tiendas que se traspasan o que han echado el cerrojo.</p>

					<p>La Administración provincial ocupada es un foco de resistencia y también de tensión. Junto a las cubiertas de neumáticos que forman su fortificación hay reservas de cócteles molótov, cuidadosamente colocados, a intervalos de pocos metros, con su mecha de trapo, a punto para ser lanzados. El Parlamento municipal de la región se reunió el jueves para aprobar cuentas y presupuestos prácticamente como si no pasara nada.</p>

					<p>El gobernador, Serguéi Taruta, que seguía la sesión, intervino unas cuantas veces, por lo general asumiendo la función de facilitador de la comunicación con los ministerios en Kiev. Este responsable quiere que, junto a las elecciones presidenciales del 25 de mayo, se celebre un referéndum nacional sobre el estatus de la lengua rusa y la descentralización del poder en Ucrania.</p>

