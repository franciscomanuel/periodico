					<ul class="listasNoticias">
						<li>Life Technologies presenta un equipo que traza mapas genéticos por 758 euros</li>
						<li>El precio abre las puertas al uso clínico de esta tecnología</li>
					</ul>	

					<hr class="suspensivos">
					<p class="autorNoticias">JAIME PRATS | 4 FEB 2012 - 00:28 CET</p>	
					<hr class="suspensivos">

					<p>Conocer el mapa genético de una persona es todavía un procedimiento demasiado caro como para que esta técnica se incorpore al resto de herramientas de diagnóstico de la medicina convencional. Aunque quizás no por mucho tiempo.</p>

					<p>La empresa estadounidense Life Technologies ha anunciado el próximo lanzamiento al mercado un equipo, llamado Ion Proton, que permite secuenciar el genoma (los entre 20.000 y 25.000 genes del ADN humano) en solo un día a un precio de 1.000 dólares (758 euros).</p>

					<p>La cantidad es importante, ya que situaría el análisis genético personalizado en el umbral de lo que se considera un coste asumible en uso clínico, apunta Xavier Estivill, coordinador del programa Genes y Enfermedad, del Centro de Regulación Genómica de Barcelona. Estivill advierte que a esta cantidad habría que añadir la interpretación de los datos, que podría multiplicar la cantidad por diez.</p>

					<p>La tecnología actual cuesta entre 2.300 y 3.800 euros. Hace dos años, la suma ascendía a  75.000 euros. Pero aún es más espectacular la caída de precios respecto al Proyecto Genoma Humano que tardó 13 años en conseguir el genoma completo (se presentó en 2003) y costó unos 2.047 millones de euros. Aunque, en este caso, se partió de cero.</p>

					<p>La secuenciación es especialmente útil en el diagnóstico de enfermedades monogénicas hereditarias (fibrosis quística, hemofilia, distrofia muscular de Duchenne). Existen unas 3.000 alteraciones genéticas conocidas vinculadas a este tipo de enfermedades que se podrían detectar de forma más sencilla y económica.</p>

					<p>Otro de los campos de aplicación clínica de esta tecnología se centra en el tratamiento de tumores de forma personalizada, especialmente en el cáncer de mama. Determinar las mutaciones en las células cancerígenas permite, en determinados, casos escoger la medicación más indicada para el paciente, lo que aumenta su eficacia y reduce los efectos secundarios no deseados.</p>

					<p>Además de Life Technologies, la empresa líder en el sector, Illumina, también ha anunciado para mitad de año una mejora de uno de sus equipos (el HiSeq 2000) que le permitirá ejecutar análisis en un día y, probablemente, a un precio cercano al Ion Proton.</p>

