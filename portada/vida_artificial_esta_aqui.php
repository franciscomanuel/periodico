					<p>Científicos de varias universidades norteamericanas y europeas han logrado “el monte Everest de la biología sintética”, como dicen los editores de Science: el primer cromosoma eucariótico fabricado en el laboratorio. Se trata de un cromosoma de levadura, el hongo que se usa para hacer cerveza, pan, biocombustible y la mitad de la investigación sobre los organismos eucariotas, como nosotros. La capacidad de introducirle un cromosoma sintético a ese organismo permitirá mejorar todo lo anterior, como hacer biocombustibles más sostenibles para el entorno o diseñar nuevos antibióticos, además de un nuevo continente de investigación sobre la pregunta del millón: cómo construir el genoma entero de un organismo superior. La reconstrucción de un neandertal, por ejemplo, sería imposible sin este paso esencial.</p>

					<img src="../img/cromosoma_artificial.png" class="imagenesNoticias">
					<footer>Fuente: Science. / HEBER LONGÁS / EL PAÍS</footer>
					<hr class="suspensivos">

					<p>La biología sintética es una disciplina emergente que trata no ya de modificar organismos, sino de diseñarlos a partir de principios básicos. En los últimos cinco años ha logrado avances espectaculares, como la síntesis artificial del genoma completo de una bacteria y varios virus. Pero esta es la primera vez que consigue fabricar un cromosoma completo y funcional de un organismo superior, o eucariota (una célula buena, en griego, la que forma los humanos). El consorcio liderado por Srinivasan Chandrasegaran, del Departamento de Ciencias de la Salud Ambiental de la Universidad Johns Hopkins, con la colaboración de Jef Boeke, presenta su rompedor resultado en la revista Science.</p>

					<p>“Nuestra investigación mueve la aguja de la biología sintética desde la teoría hasta la realidad”, dice Boeke, uno de los pioneros de este campo. “Este trabajo representa el mayor paso que se ha dado hasta la fecha en el esfuerzo internacional para construir el genoma completo de una levadura sintética”.</p>

					<p>Boeke empezó este proyecto hace siete años en otra universidad, la Johns Hopkins de Baltimore, enrolando a 60 estudiantes universitarios en un proyecto llamado Build a genome (construye un genoma). Las técnicas para sintetizar ADN han mejorado mucho en la última década, pero suelen producir tramos bastante cortos de secuencia, no mucho más allá de 100 o 200 letras (tgaagcct…). Los estudiantes se ocuparon de ir pegando esas secuencias sintéticas en tramos cada vez mayores. El cromosoma final mide cerca de 300.000 letras.</p>

					<p>Que un hito científico se refiera a la levadura (Saccharomyces cerevisiae), un hongo unicelular que ya utilizaban los antiguos egipcios para hacer la cerveza, parece una buena paradoja o un mal chiste, pero no es así. La división fundamental entre todos los seres vivos de la Tierra no es la que existe entre plantas y animales, ni entre microorganismos y especies grandes o macroscópicas: es entre procariotas (bacterias y arqueas) y eucariotas (todos los demás, incluidos nosotros).</p>

					<p>Y lo importante de la levadura es que, por mucho que sea un organismo unicelular, cae en nuestro lado de la barrera. No es exagerado decir que la mayor parte de lo que sabemos sobre la biología humana se debe a la investigación de este familiar hongo de apariencia modesta. La levadura tiene unos 6.000 genes, y comparte un tercio de ellos con el ser humano, pese a los 1.000 millones de años de evolución que nos separan.</p>

					<p>Los cromosomas son los paquetes en que se reparte el genoma de los organismos superiores, o eucariotas. Son mucho más que un trozo de ADN: están empaquetados en complejas arquitecturas formadas por centenares de proteínas que interactúan con el material genético, como las histonas. Están dotados de un centrómero, la maquinaria especializada en distribuir una copia del genoma a cada célula hija en cada ciclo de división celular; y sus extremos están protegidos por unos sistemas singulares, los telómeros, que garantizan la integridad de la información genética en cada ciclo de replicación. De ahí que el logro actual vaya mucho más allá que la síntesis del genoma de una bacteria que se había logrado hasta ahora.</p>

					<p>Los humanos tenemos el genoma dividido en 23 cromosomas (o pares de cromosomas); la levadura lo tiene distribuido en 16, y los científicos se han centrado en el más pequeño de ellos, el número 3. Han extraído al hongo su cromosoma 3 natural y lo han sustituido por su versión sintética, llamada synIII, que cubre las funciones de su colega natural pese a estar extensivamente alterado con toda clase de elementos artificiales diseñados para facilitar su manipulación en el futuro inmediato.</p>

					<p>Que el cromosoma sintético funcione en su entorno natural, una célula viva de levadura, es el verdadero hito del trabajo, según los investigadores. “Hemos mostrado”, dice Boeke, “que las células de levadura que llevan el cromosoma sintético son notablemente normales; se comportan de forma casi idéntica a las levaduras naturales, salvo por que ahora poseen nuevas capacidades y pueden hacer cosas que sus versiones silvestres no pueden hacer”.</p>

					<p>La versión natural del cromosoma 3 de Saccharomyces cerevisiae tiene 316.667 bases (las letras del ADN a, g, t, c). La versión sintética es un poco más corta, con 273.871 bases, como consecuencia de las más de 500 alteraciones que los científicos han introducido en él. Entre estas modificaciones se encuentra la eliminación de muchos tramos de ADN repetitivo que no tienen función alguna, ya estén situados entre un gen y otro (secuencias intergénicas) o dentro mismo de los genes (intrones).</p>

					<p>También han eliminado los transposones, o genes que saltan de una posición a otra en el genoma de todos los organismos eucariotas. El cromosoma artificial synIII también lleva muchos tramos de ADN añadidos por los investigadores. El número total de cambios de un tipo u otro se acerca a los 50.000, pese a lo cual el cromosoma sintético sigue siendo funcional.</p>

					<p>Pese a sus evidentes implicaciones para la biología fundamental –¿puede construirse el genoma de un organismo superior, incluido el ser humano, a partir de compuestos químicos sacados de un bote de la estantería?—, el proyecto tiene sobre todo objetivos aplicados. Y no solo en las áreas industriales, como la fabricación de pan y bebidas, en las que este organismo se ha utilizado siempre.</p>

					<p>Una de las aplicaciones que resaltan los autores es la mejora en la manufactura de medicinas como la artemisina para la malaria o la vacuna para la hepatitis B. Como la mayoría de los antibióticos provienen de hongos, y la levadura es uno de ellos, también cabe predecir avances en el diseño y producción de estos medicamentos.</p>

					<p>Más a largo plazo, las levaduras sintéticas pueden facilitar la síntesis de medicamentos anticancerosos como el Taxol, cuya vía de síntesis es tan complicada e implica a tantos genes que supone un formidable escollo para las tecnologías convencionales. En un área industrial muy distinta, esta tecnología, según esperan sus autores, servirá para desarrollar biocombustibles más eficaces que los actuales, entre ellos alcoholes como el butanol, y también diésel de origen biológico.</p>

					<p>Y, por supuesto, synIII es solo el primero de los 16 cromosomas de la levadura que los investigadores logran sintetizar. Los intentos de repetir la hazaña con los otros 15 cromosomas ya están en proyecto, y forman parte de un programa internacional llamado Sc 2.0 que implica a científicos de Estados Unidos, China, Australia, Singapur y el Reino Unido. En el nombre del proyecto, Sc es por Saccharomyces cerevisiae, el nombre científico de la levadura de la cerveza, y el 2.0 quiere enfatizar lo mucho que los seres vivos están a punto de parecerse a cualquier otro desarrollo tecnológico. El objetivo es construir un genoma completo de levadura, o el primer organismo complejo sintetizado en el tubo de ensayo.</p>

					<p>Echando la vista más hacia el futuro, cabe especular sobre la resurrección de especies extintas como el mamut o el neandertal, cuyos genomas ya han sido secuenciados a partir de sus restos fósiles. Si estos proyectos llegan a abordarse alguna vez, tendrán que basarse en una técnica similar a la que Boeke y sus colegas acaban de poner a punto para este engañosamente simple hongo que tan servicial ha resultado a la especie humana desde los albores del neolítico.</p>
					
	

