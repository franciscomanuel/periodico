					<h1 class="titulosNoticias">Un Barça a pedazos</h1>

					<ul class="listasNoticias">
						<li>Con la Liga cuesta arriba, las ocho bajas azulgrana marcan el duelo con el Athletic</li>
						<li>La falta de liderazgo e ideario destroza al Barcelona</li>
