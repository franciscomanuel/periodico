					<hr class="suspensivos">
					<p class="autorNoticias">JORDI QUIXANO | Barcelona | 19 ABR 2014 - 22:43 CET</p>	
					<hr class="suspensivos">

					<br>

					<img src="../img/martino_rueda_prensa.jpg" class="imagenesNoticias">
					<footer>Martino, durante la rueda de prensa.</footer>
					<hr class="suspensivos">

					<br>	

					<p>Comparece ante el Athletic el Barça o lo que queda de él (21.00, C+), porque entre lesionados y sancionados tiene ocho bajas. Los varapalos sufridos en una semana han hecho que el equipo se dé de bruces con una realidad siempre negada, sobre todo porque su gen competitivo no parecía tener final, por más que el Bayern lo advirtiera el curso pasado con una expresiva goleada en semifinales europeas. Ahora, tercero en una Liga cuesta arriba y apeado de los torneos eliminatorios por sus dos rivales, el Madrid y el Atlético, es el técnico el que entona un discurso apagado, autocrítico y hasta depresivo. “No tengo ninguna satisfacción con mi trabajo, en absoluto”, reflexionó Martino; “cuando los resultados no se dan, nadie puede estar satisfecho. Es un equipo que gana y más allá de la Supercopa, el hecho de no tener chances en dos competencias y muy pocas en la Liga… hace que no pueda rescatar nada positivo de mi trabajo”.</p>

					<p>La misma sensación tiene el equipo, aunque sisea que con Valdés y Piqué enteros la historia habría sido otra. Síntoma, en cualquier caso, de que ya no es un equipo con fondo de armario, que la clase media solo funciona con los extremos, opción que Martino descartó en beneficio de Cesc y Neymar. Se impone una revolución, supeditada a la pronunciación final de la FIFA o del TAS, toda vez que por el momento el club está castigado (por incumplir las normas en las contrataciones de menores) a no fichar hasta el verano de 2015. Pero en el Camp Nou ya se han disparado las quinielas de los jugadores convenientes para reforzar al equipo, al menos del entrenador, incorporación aprobada por la FIFA. En el club la sensación es que no seguirá Martino —“no tengo nada que comunicar, sino dar la cara por los resultados negativos. Mi atención está en esta temporada y lo contrario sería traicionar al grupo”, afirmó— y se sabe que en la secretaría técnica, como propuso en el curso anterior, gustan Ernesto Valverde (Athletic) y Luis Enrique (Celta), del mismo modo que se descarta a Jürgen Klopp (Borussia Dortmund). También se asegura que la potestad para reconfigurar al equipo será del director deportivo, Andoni Zubizarreta, con la intención de que no se repitan los errores del pasado, cuando el expresidente Rosell decidió el técnico y se inmiscuyó en el fichaje fracasado de un central, con David Luiz como primera opción. “Habrá una remodelación profunda liderada por Zubi; no una revolución”, aseveró ayer Bartomeu en el programa Gol a Gol.</p>

					<p>Pero el equipo apenas se aguanta de pie, entre las lesiones y la sanción de Busquets, completada la lista con Afellay y Sergi Gómez, del filial. A las prolongadas bajas de Cuenca y Dos Santos, se suma de nuevo Puyol, que ha recaído de su maltrecha rodilla. Tampoco estarán Piqué y Valdés, ni Alba (rotura fibrilar en el bíceps femoral de la pierna derecha) ni Neymar, que sufre un edema en el cuarto metatarsiano del pie izquierdo. “No creo que vuelva a jugar un partido con el Barça este año”, aclaró Tata, a la vez que el 11 ya cambió su imagen en Twitter con la elástica de Brasil, anfitrión del Mundial, que sí podrá disputar.</p>	

					<p>Aunque sí hay descontento con Messi, puesto que le han procurado las condiciones exigidas, desde el entrenador al modo de jugar, y no ha respondido en los momentos definitivos. Y la incógnita es que nadie sabe qué le pasa, ni en el vestuario ni en las oficinas. “Los futbolistas a veces tienen muy buen rendimiento y a veces, no. No hay que darle importancia si a veces juega mal. Pero tratándose de él, a la gente le cuesta entenderlo”, manifestó Martino, que siempre le elogió pero que cambió el discurso. Aunque Bartomeu fue claro: “El socio debe entender que hay que mimar a un Leo que debe liderar el futuro de este club. Y hay que ser justos con él porque nos ha llevado a los triunfos de los últimos años”.</p>

					<p>“Nadie puede pensar que estas derrotas son un trámite. Son golpes que duelen y decepcionan. Todo resultado negativo tiene olor a sentencia final”, apuntó Martino, que no quiso señalar al grupo: “No hace falta hacer cambios muy profundos, creo que Bartra, Piqué, Alba, Pedro, Cesc, Andrés, Leo, Neymar… tienen una edad muy importante para seguir compitiendo sin problemas, con mucho futuro y carrera por delante”. Pero poco les queda este curso.</p>	

					<p>Algunos de la directiva del Barcelona se echaron las manos a la cabeza cuando se decidió exhibir el otro día en el Camp Nou una pancarta donde se podía leer La Masia no se toca en catalán. Todo un pulso a la FIFA, que sancionó la semana pasada al club con no poder inscribir jugadores en los dos próximos periodos válidos para tal efecto, después de que se realizaran fichajes ilegales de menores. “Una barbaridad”, cuentan en el Barça, sobre todo porque una de las directrices de la comisión jurídica es no enfadar a la FIFA. La otra máxima es no explicar nada de las alegaciones presentadas. Hermetismo puro, toda vez que a lo que aspiran es a regular la situación, zanjarlo con una sanción económica.</p>

					<p>“Por eso no queremos ni siquiera hablar de la posibilidad del TAS”, admiten en el club, al tiempo que, aunque el discurso público sea el opuesto, reconocen que el Barça sí ha hecho algo mal en este caso. Y no son pocos los que se acuerdan de una frase en una de las juntas, cuando alguien dijo: “Estamos en falso”.</p>

					<p>Defiende el Barcelona que no han incumplido la ley porque la FIFA delega sus funciones en la Federación Española y esta en la Catalana, que sí validó las fichas. Lo hizo porque el Barça, como era costumbre antes de que se denegaran las fichas en 2013, nombraba a un tutor que hacía las veces de familia del niño. “Si nos hubieran dicho que era irregular, pues no lo habríamos hecho y hubiésemos buscado una solución”, apuntan desde el club. Pero hay quien recuerda que desde la presidencia se dijo que la FIFA haría la vista gorda, que no le iban a decir nada a una entidad tan poderosa como el Barcelona. Así, entre otras cosas, el club azulgrana obvió conversar con el Transfer Matching System (TMS) —organismo creado por la FIFA en 2009 para evitar irregularidades en las transacciones, donde existe una subcomisión del Estatuto de Jugadores de la FIFA para aprobar o no las solicitudes— y ahora se dan de bruces con la realidad de una sanción que podían intuir pero que no querían valorar.</p>

					<p>Quizá por eso, el verano pasado, el Barcelona envió una carta a la FIFA firmada por el entonces presidente Sandro Rosell. No era exactamente un estado de excepción, como explicó en su día el actual presidente, Josep Maria Bartomeu, sino una solicitud de homologación o regulación del artículo 19, que dice que ningún menor de 16 años puede fichar por un club extranjero salvo en tres casos: que los padres se trasladen al país del nuevo club por razones no relacionadas con el fútbol; que el menor viva a un máximo de 50 kilómetros de la frontera y el club esté a la misma distancia de dicha frontera; o que si es ciudadano de la UE y tiene entre 16 y 18 años, el club está obligado a proporcionar formación escolar y alojamiento.</p>

					<p>“El artículo es inaplicable. No solo por la inmigración natural, sino porque la FIFA no puede aplicar lo mismo a un equipo de Primera que a otro de Tercera. Por eso pedimos que se regulara. Incluso se podría poner un cupo de jugadores por año”, desvelan en el Barça. Aunque no se llegó a tanta precisión porque no hubo lugar al diálogo. Y abundan: “Si un niño de EE UU quiere estudiar en Barcelona, puede; pero no jugar a fútbol. Es una ley que choca con el punto de vista social”.</p>

