					<p>Tras su vuelta al poder en 2012, Vladímir Putin ha practicado una "política de alejamiento" de Occidente porque, tras las manifestaciones de protesta masivas que acompañaron su retorno a la presidencia, "llegó a la conclusión de que seguir acercándose a Occidente destrozaría su régimen". Así lo manifiesta Alexéi Arbátov, jefe del Centro de Seguridad Internacional del Instituto de Economía Mundial y Relaciones Internacionales (IMEMO en ruso) de la Academia de Ciencias de Rusia. "Putin cree que puede haber una revolución contra él y que Occidente la fomenta. Si pensara de otro modo, debería reconocer que es un mal dirigente, porque lleva 14 años en el poder", señala.</p>

					<p>"Putin no quiere una Guerra Fría, quiere que haya comunicación y foros multilaterales, pero ha cortado el camino a Occidente porque entiende que éste supone la desintegración del régimen", dice Arbátov en vísperas de un viaje a EE UU.</p>

					<p>La retórica hostil, suspicaz y desconfiada del alejamiento ha ido "aumentando como una bola de nieve y ha creado una oleada reaccionaria", dice el especialista, que distingue entre "confrontación" y "alejamiento". Al experto le preocupa el futuro del proceso de desarme entre Rusia y EEUU y la falta de iniciativas en este sector, donde, incluso en los peores tiempos, hubo planes para seguir reduciendo el techo de la confrontación. "No es posible ponerse de acuerdo sobre armas estratégicas y al mismo tiempo realizar un enorme programa de modernización de estas armas", dice refiriéndose a su propio país. "Un programa de rearme y equipamiento por valor de más de 408 mil millones de euros hasta 2020 debe ser justificado, y, para justificarlo, se invoca la amenaza de EE UU y de la OTAN, y se sugiere, sin decirlo abiertamente que China también es una amenaza".</p>

					<p>"Las conversaciones sobre armas estratégicas han llegado a un callejón sin salida. En 2010 firmamos un acuerdo y lo cumplimos, pero se esperaba que hubiera una nueva etapa y que llegaríamos a un acuerdo sobre la defensa antimisiles. El acuerdo sobre armas estratégicas está en vigor, pero no se mantienen conversaciones para el futuro sobre nada más", señala. "Incluso en los años de la Guerra Fría, comenzando por el primer acuerdo sobre Defensa Antimisiles de 1972, cada vez que firmábamos un nuevo tratado, ya se sabía cuál sería el siguiente en la agenda y preparábamos las posiciones para las siguientes etapas, y en total hemos firmado 9 acuerdos y tratados en el campo de las armas nucleares", dice.</p>

					<p>"Si la confrontación política continúa agudizándose, Rusia y EEUU dejarán de informarse sobre los lanzamientos de misiles". Por el momento, sin embargo, "se informan y hacen 15 inspecciones al año a las bases militares. El problema es que los acuerdos, aparte de las medidas de verificación, implican colaboración, transparencia, y confianza y si esto no continúa y cada uno se va a su rincón, al cabo de tres años, no sabrán lo que hace la otra parte", dice.</p>

					<p>Tanto Arbátov como su partido (Yábloko) están en contra de la anexión de Crimea, aunque "el entorno de Vladímir Putin considera que Rusia ha violado mucho menos la legislación internacional que la OTAN cuando bombardeó Serbia". "Por primera vez desde 1945, un Estado se anexionó parte de otro. Se puede criticar esto, pero Rusia no bombardeó a nadie, no provocó flujos de centenares de miles de refugiados como la OTAN en Kósovo", afirma. Las incertidumbres siguen abiertas. Tras la operación de Crimea "Putin no puede permitirse ignorar al resto de los rusos y permitir que los maten o los persigan", dice refiriéndose a las zonas del Este de Ucrania en particular y al espacio postsoviético en general.</p>

					<p>La OTAN ha cortado la colaboración con Rusia, y esto afecta a un amplio espectro de actividades y también los esfuerzos de gente como Arbátov que "durante 20 años hemos estado intentando establecer una colaboración normal con la Alianza Atlántica, desde una defensa antiaérea conjunta, a una defensa antimisiles común a una interoperabilidad, compatibilidad operativa, dirección, comunicaciones, calibres y un cuerpo común de reacción rápida de unas 200.000 personas que pudiera mandar de uno a tres batallones a Afganistán o a Sierra Leona, contra extremistas, por ejemplo, para acabar con una amenaza terrorista". "Teóricamente teníamos tareas comunes, como la lucha contra el terrorismo o contra la difusión de armas de destrucción masiva, pero no resultó", afirma el experto, según el cual "la colaboración entre Rusia y la OTAN era sobre todo simbólica, había maniobras conjuntas en el Mediterráneo, visitas, algunos ejercicios de defensa antiaérea y lucha contra los secuestradores de aviones. Como era simbólico no es un gran daño", afirma.</p>

					<p>En lo que se refiere a Afganistán la colaboración en el tránsito de tropas por territorio ruso no se interrumpe, entiende Arbátov, aunque hay alguna señal en sentido contrario procedente de funcionarios anónimos en Bruselas. En su opinión, "el tránsito hacia Afganistán y la preparación del Ejército afgano" son algo "sagrado".</p>


